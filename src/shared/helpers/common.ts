export function generateRandomValue(min:number, max: number, numAfterDigit = 0) {
  return +((Math.random() * (max - min)) + min).toFixed(numAfterDigit);
}

export function getRandomItems<T>(items: T[]):T[] {
  const startPosition = generateRandomValue(0, items.length - 1);
  const endPosition = startPosition + generateRandomValue(startPosition, items.length);
  return items.slice(startPosition, endPosition);
}

export function getRandomItem<T>(items: T[]):T {
  return items[generateRandomValue(0, items.length - 1)];
}

export function getExactItems(prefix: string, suffix: string, length: number):string[] {
  return Array.from({length}).map((_, index) => `${prefix}${index + 1}${suffix}`);
}

export function getRandomBoolean():boolean {
  return Boolean(generateRandomValue(0, 1));
}

export function getRandomUserPassword(passwordLength: number = 8): string {
  const chars = '0123456789abcdefghijklmnopqrstuvwxyz!@#$%^&*()ABCDEFGHIJKLMNOPQRSTUVWXYZ';
  let password = '';
  for (let i = 0; i <= passwordLength; i++) {
    const randomNumber = Math.floor(Math.random() * chars.length);
    password += chars.substring(randomNumber, randomNumber + 1);
  }
  return password;
}

export function getErrorMessage(error: unknown): string {
  return error instanceof Error ? error.message : '';
}
